/**
 * Data Transfer Object (DTO) for parameter ID.
 *
 * @interface IParamIdDto
 * @property {string} id - The unique identifier.
 */
export interface IParamIdDto {
  id: string;
}

/**
 * Interface representing a parameter token DTO (Data Transfer Object).
 *
 * @interface IParamTokenDto
 * @property {string} token - The token string.
 */
export interface IParamTokenDto {
  token: string;
}
/**
 * Interface representing a parameter video ID DTO (Data Transfer Object).
 *
 * @interface IVideoIdParamDto
 * @property {string} videoId - The unique identifier for the video.
 */
export interface IVideoIdParamDto {
  videoId: string;
}
